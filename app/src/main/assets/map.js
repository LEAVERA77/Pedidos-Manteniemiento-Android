const PMG_COORD_TITULO = {
    inchauspe: 'Campo Inchauspe 1969',
    posgar94: 'POSGAR 1994',
    posgar98: 'POSGAR 1998',
    posgar2007: 'POSGAR 2007'
};

let _pmgProjRegistrado = false;
export function asegurarDefsProyeccionesARG() {
    if (_pmgProjRegistrado || typeof proj4 === 'undefined') return;
    _pmgProjRegistrado = true;
    Object.keys(PMG_AR_PROJ4).forEach(fam => {
        PMG_AR_PROJ4[fam].forEach((defStr, i) => {
            proj4.defs('PMG_' + fam + '_Z' + (i + 1), defStr);
        });
    });
}

/** Faja 1–7 según longitud WGS84 (franjas ~3° al oeste de Argentina continental). */
export function fajaArgentinaPorLongitud(lon) {
    const w = Number(lon);
    if (!isFinite(w)) return 4;
    if (w < -70.5) return 1;
    if (w < -67.5) return 2;
    if (w < -64.5) return 3;
    if (w < -61.5) return 4;
    if (w < -58.5) return 5;
    if (w < -55.5) return 6;
    return 7;
}

export function registrarFajaInstalacionSiFalta(lon) {
    try {
        if (localStorage.getItem('pmg_faja_instl') != null) return;
        const w = Number(lon);
        if (!isFinite(w)) return;
        localStorage.setItem('pmg_faja_instl', String(fajaArgentinaPorLongitud(w)));
    } catch (_) {}
}

export function resolverFajaProyeccion(modo, lngPedido) {
    const m = (modo || 'punto').trim();
    if (/^[1-7]$/.test(m)) return parseInt(m, 10);
    if (m === 'instal') {
        try {
            const z = parseInt(localStorage.getItem('pmg_faja_instl') || '', 10);
            if (z >= 1 && z <= 7) return z;
        } catch (_) {}
        return fajaArgentinaPorLongitud(lngPedido);
    }
    return fajaArgentinaPorLongitud(lngPedido);
}

/**
 * Proyecta WGS84 según empresa_config (visible en detalle / impresión / export).
 * Retorna { titulo, z, modoTxt, crsLinea, lx, ly, vx, vy } o null.
 * Etiquetas cortas: crsLinea = sistema + faja; lx/ly = X/Y en metros.
 */
export function proyectarCoordPedido(lat, lng) {
    const cfg = window.EMPRESA_CFG || {};
    const fam = (cfg.coord_proy_familia || 'none').trim();
    if (!fam || fam === 'none' || typeof proj4 === 'undefined') return null;
    const arr = PMG_AR_PROJ4[fam];
    if (!arr || !isFinite(lat) || !isFinite(lng)) return null;
    const modo = (cfg.coord_proy_modo || 'punto').trim();
    const z = resolverFajaProyeccion(modo, lng);
    asegurarDefsProyeccionesARG();
    const defName = 'PMG_' + fam + '_Z' + z;
    let e;
    let n;
    try {
        [e, n] = proj4('EPSG:4326', defName, [lng, lat]);
    } catch (err) {
        console.warn('[coords]', err);
        return null;
    }
    const titulo = PMG_COORD_TITULO[fam] || fam;
    const modoTxt = /^[1-7]$/.test(modo) ? 'F' + z + ' fija' : (modo === 'instal' ? 'F' + z + ' (1.ª GPS)' : 'F' + z);
    const crsLinea = 'Faja ' + z;
    const lx = 'X (m)';
    const ly = 'Y (m)';
    const vx = e.toFixed(3).replace('.', ',');
    const vy = n.toFixed(3).replace('.', ',');
    return { titulo, z, modoTxt, crsLinea, lx, ly, vx, vy, e, n };
}
if (typeof window !== 'undefined') window.proyectarCoordPedido = proyectarCoordPedido;

export function tieneProyeccionEmpresaConfigurada() {
    const fam = String((window.EMPRESA_CFG || {}).coord_proy_familia || 'none').trim();
    return !!fam && fam !== 'none';
}

export function leerPreferenciaCoordsDisplayNuevoPedido() {
    if (!tieneProyeccionEmpresaConfigurada()) return 'wgs84';
    try {
        const s = localStorage.getItem('pmg_coords_display_pref');
        if (s === 'wgs84' || s === 'proyectadas') return s;
    } catch (_) {}
    return 'wgs84';
}

export function convertirAInchauspe(lat, lng) {
    try {
        asegurarDefsProyeccionesARG();
        const z = fajaArgentinaPorLongitud(lng);
        const defName = 'PMG_inchauspe_Z' + z;
        const xy = proj4('EPSG:4326', defName, [lng, lat]);
        return {
            x: xy[0].toFixed(2).replace('.', ','),
            y: xy[1].toFixed(2).replace('.', ',')
        };
    } catch (e) {
        console.error('Error al convertir coordenadas:', e);
        return { x: 'Error', y: 'Error' };
    }
}